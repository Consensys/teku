  /**
   * Addition chain for multiplication by 0xd201000000010000 == -x, the BLS parameter.
   *
   * @param p the point to be multiplied
   * @return -x * p
   */
  static JacobianPoint mxChain(JacobianPoint p) {
    JacobianPoint q = p.dbl();
    int[] ndoubles = {2, 3, 9, 32, 16};
    for (int n : ndoubles) {
      q = q.add(p);
      for (int j = 0; j < n; j++) {
        q = q.dbl();
      }
    }
    return q;
  }
